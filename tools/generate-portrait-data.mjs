import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const portrait = await readFile(join(projectRoot, 'portrait.jpg'));
const dataUri = `data:image/jpeg;base64,${portrait.toString('base64')}`;
const output = `// Generated by tools/generate-portrait-data.mjs. Do not edit manually.\nwindow.BBX_PORTRAIT_DATA_URI = ${JSON.stringify(dataUri)};\n`;

await writeFile(join(projectRoot, 'portrait-data.js'), output);
